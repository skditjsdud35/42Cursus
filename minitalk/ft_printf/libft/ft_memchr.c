#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	index;

	index = 0;
	while (index < n)
	{
		if (*(const unsigned char *)s == (unsigned char)c)
			return ((unsigned char *)s);
		index++;
		s++;
	}
	return (NULL);
}
